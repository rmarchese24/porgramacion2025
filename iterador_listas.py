# Iteradores

lenguajes = ["Python", "Javascript", "PHP", "C++", "Java"]
print (lenguajes)

# para mostrar elemento por elemnto  arreglo
print (lenguajes[0])
print (lenguajes[1])
print (lenguajes[2])
print (lenguajes[3])
print (lenguajes[4])

# Iterar  - ventajas de usar un iterador (for)
for lenguajes in lenguajes:
    print (f"Estoy Aprendiendo programacion con el lenguaje: {lenguajes}") 
    

# para contar los elementos de la lista 
print(len(lenguajes))